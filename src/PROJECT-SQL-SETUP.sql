CREATE TABLE PointOfInterest(
    poiAddr VARCHAR(70), poiName VARCHAR(50), rating BINARY_DOUBLE, 
    CONSTRAINT POI_pk PRIMARY KEY (poiAddr,poiName)
); 

CREATE TABLE School(
    poiAddr VARCHAR(70), 
    poiName VARCHAR(50),
    schoolType VARCHAR(20),
    numStudents NUMBER(10), 
    CONSTRAINT School_pk PRIMARY KEY (poiAddr, poiName), 
    CONSTRAINT POI_scl_fk FOREIGN KEY (poiAddr, poiName) REFERENCES PointOfInterest(poiAddr,poiName) ON DELETE CASCADE 
