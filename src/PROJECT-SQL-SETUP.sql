CREATE TABLE HasPlayground(
    parkStatus VARCHAR(20), hasPlayground CHAR(1), 
    CONSTRAINT HasPg_pk PRIMARY KEY (parkStatus)
); 

CREATE TABLE Park(
    poiAddr VARCHAR(70), poiName VARCHAR(50), parkStatus VARCHAR(20), 
    CONSTRAINT Park_pk PRIMARY KEY (poiAddr, poiName), 
    CONSTRAINT POI_Park_fk FOREIGN KEY (poiAddr, poiName) REFERENCES PointOfInterest(poiAddr,poiName) ON DELETE CASCADE, 
    CONSTRAINT ParkSt_fk FOREIGN KEY (parkStatus) REFERENCES HasPlayground(parkStatus)
