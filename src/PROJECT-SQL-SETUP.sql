CREATE TABLE Attraction(
    poiAddr VARCHAR(70), poiName VARCHAR(50), price NUMBER(10), capacity NUMBER(10), 
    CONSTRAINT Attr_pk PRIMARY KEY (poiAddr, poiName), 
    CONSTRAINT POI_Attr_fk FOREIGN KEY (poiAddr, poiName) REFERENCES PointOfInterest(poiAddr,poiName)  ON DELETE CASCADE
