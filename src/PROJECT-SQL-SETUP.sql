CREATE TABLE VehicleType(
    type VARCHAR(8), topSpeed NUMBER(10), capacity NUMBER(10), frequency NUMBER(10), 
    CONSTRAINT VehicleType_pk PRIMARY KEY (type)
); 

CREATE TABLE Garage(
    name VARCHAR(50) NOT NULL, capacity NUMBER(10), type VARCHAR(8), 
    CONSTRAINT Garage_pk PRIMARY KEY (name),
    CONSTRAINT Type_fk FOREIGN KEY (type) REFERENCES VehicleType(type) 
