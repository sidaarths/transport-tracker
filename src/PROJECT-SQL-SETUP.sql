CREATE TABLE LineStops(
    lineCode VARCHAR(20), stopNum NUMBER(10), stopOrder NUMBER(10), 
    CONSTRAINT LineStops_pk PRIMARY KEY (lineCode, stopNum), 
    CONSTRAINT line_fk FOREIGN KEY (lineCode) REFERENCES TransitLine(lineCode) ON DELETE CASCADE, 
    CONSTRAINT LineStop_fk FOREIGN KEY (stopNum) REFERENCES Stop(num) ON DELETE CASCADE
