CREATE TABLE Restaurant(
    poiAddr VARCHAR(70), poiName VARCHAR(50), capacity NUMBER(10), cuisine VARCHAR(20), 
    CONSTRAINT Restaurant_pk PRIMARY KEY (poiAddr, poiName), 
    CONSTRAINT POI_Rest_fk FOREIGN KEY (poiAddr, poiName) REFERENCES PointOfInterest(poiAddr,poiName) ON DELETE CASCADE
