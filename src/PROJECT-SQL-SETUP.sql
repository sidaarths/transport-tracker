CREATE TABLE Stop(
    num NUMBER(10) NOT NULL, lat BINARY_DOUBLE, 
    lon BINARY_DOUBLE, name VARCHAR(50), 
    CONSTRAINT Stop_pk PRIMARY KEY (num)
); 

CREATE TABLE Visited(
    accountID NUMBER(10) NOT NULL, 
    stopNum NUMBER(10) NOT NULL, 
    visitedDate DATE, 
    CONSTRAINT Visited_pk PRIMARY KEY (accountID, stopNUM, visitedDate), 
    CONSTRAINT Acct_fk FOREIGN KEY (accountID) REFERENCES Account(ID) ON DELETE CASCADE, 
