CREATE TABLE TransitLine(
    lineCode VARCHAR(20) NOT NULL, lineName VARCHAR(50),garageName VARCHAR(50), startStop NUMBER(10), endStop NUMBER(10),
    CONSTRAINT transitLine_pk PRIMARY KEY (lineCode),
    CONSTRAINT LineGarage_fk FOREIGN KEY (garageName) REFERENCES Garage(name), 
    CONSTRAINT startStop_fk FOREIGN KEY (startStop) REFERENCES Stop(num), 
    CONSTRAINT endStop_fk FOREIGN KEY (endStop) REFERENCES Stop(num) 
